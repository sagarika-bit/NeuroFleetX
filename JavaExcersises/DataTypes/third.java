package JavaExercise.Datatypes;

public class third {
  public static void main(String[] args) {
        byte b = 10;
        short s = 500;
        long l = 20000L;
        float f = 3.14f;
        char c = 'J';

        System.out.println("byte: " + b);
        System.out.println("short: " + s);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("char: " + c);
    }
}
